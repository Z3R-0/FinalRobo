package OrderInfo;

public class Customer {
    String surName;
    String lastName;
    String adress;
    String zipCode;
    String city;
    
    //CONSTRUCTOR
	public Customer(String surName, String lastName, String adress, String zipCode, String city) {
		this.surName = surName;
		this.lastName = lastName;
		this.adress = adress;
		this.zipCode = zipCode;
		this.city = city;
	}	

}
